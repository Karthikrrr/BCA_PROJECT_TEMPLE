package com.business_website.dto;

public class prasadamDto {

    private String templeName;
    private String userName;
    private String email;
    private String date;
    private String time;
    private String phoneNumber;
    private int numberOfPasadam;
    private String totalAmount;

    
    public String getTempleName() {
        return templeName;
    }
    public void setTempleName(String templeName) {
        this.templeName = templeName;
    }
    public String getUserName() {
        return userName;
    }
    public void setUserName(String userName) {
        this.userName = userName;
    }
    public String getEmail() {
        return email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getDate() {
        return date;
    }
    public void setDate(String date) {
        this.date = date;
    }
    public String getTime() {
        return time;
    }
    public void setTime(String time) {
        this.time = time;
    }
    public String getPhoneNumber() {
        return phoneNumber;
    }
    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
    public int getNumberOfPasadam() {
        return numberOfPasadam;
    }
    public void setNumberOfPasadam(int numberOfPasadam) {
        this.numberOfPasadam = numberOfPasadam;
    }
    public String getTotalAmount() {
        return totalAmount;
    }
    public void setTotalAmount(String totalAmount) {
        this.totalAmount = totalAmount;
    }

}
